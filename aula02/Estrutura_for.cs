using System;

namespace OlaMundo
{
    class EstruturaFor
    {
        static void Main()
        {
            for (int n1 = 1; n1 <= 10; n1++)
            {
                Console.WriteLine(n1);
            }
        }
    }
}

/*
Comentários explicativos:

1. `for (int n1 = 1; n1 <= 10; n1++)` define uma estrutura de repetição for que inicializa a variável `n1` com o valor 1, executa o loop enquanto `n1` for menor ou igual a 10 e incrementa `n1` em 1 a cada iteração.

2. `Console.WriteLine(n1);` exibe o valor atual da variável `n1` a cada iteração do loop.
*/

