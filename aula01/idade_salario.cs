using System;

namespace OlaMundo
{
    class VariaveisIdadeSalario
    {
        static void Main()
        {
            var idade = 25;
            var salario = 1285.76;

            Console.WriteLine("Idade {0}, Salário {1}", idade, salario);
        }
    }
}

/*
Comentários explicativos:

1. `var idade = 25;` declara uma variável `idade` e a inicializa com o valor 25.

2. `var salario = 1285.76;` declara uma variável `salario` e a inicializa com o valor 1285.76.

3. `Console.WriteLine("Idade {0}, Salário {1}", idade, salario);` exibe a idade e o salário na tela, utilizando formatação de string.
*/

